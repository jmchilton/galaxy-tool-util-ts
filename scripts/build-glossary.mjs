#!/usr/bin/env node
/**
 * Merge galaxy-terms.yml + tool-util-terms.yml → docs/glossary.md
 *
 * Galaxy terms are nested (category.subcategory.term: description).
 * Tool-util terms are flat (category.term: description).
 * We flatten both into a sorted list of {term, description, source} entries.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

// Resolve yaml from a workspace package that depends on it.
const require = createRequire(
  resolve(dirname(fileURLToPath(import.meta.url)), "../packages/schema/package.json"),
);
const YAML = require("yaml");

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const GLOSSARY_DIR = resolve(ROOT, "docs/glossary");
const OUTPUT = resolve(ROOT, "docs/glossary.md");

/** Recursively extract leaf string values with their key path. */
function extractTerms(obj, path = [], source = "galaxy") {
  const entries = [];
  for (const [key, value] of Object.entries(obj)) {
    if (typeof value === "string") {
      entries.push({ term: key, path: [...path], description: value.trim(), source });
    } else if (typeof value === "object" && value !== null) {
      entries.push(...extractTerms(value, [...path, key], source));
    }
  }
  return entries;
}

/** Known acronyms that should stay uppercase. */
const ACRONYMS = new Set([
  "trs", "md5", "sha1", "sha256", "sha512", "url", "api", "xml", "json", "cors", "ga4gh",
]);

/** Convert camelCase/snake_case to Title Case, preserving acronyms. */
function titleCase(s) {
  return s
    .replace(/([a-z])([A-Z])/g, "$1 $2")
    .replace(/_/g, " ")
    .split(" ")
    .map((w) =>
      ACRONYMS.has(w.toLowerCase()) ? w.toUpperCase() : w.charAt(0).toUpperCase() + w.slice(1),
    )
    .join(" ");
}

/** Group path to a readable category string. */
function categoryLabel(path) {
  if (path.length === 0) return "General";
  return path.map(titleCase).join(" > ");
}

// --- Load sources ---
const galaxyYaml = readFileSync(resolve(GLOSSARY_DIR, "galaxy-terms.yml"), "utf-8");
const toolUtilYaml = readFileSync(resolve(GLOSSARY_DIR, "tool-util-terms.yml"), "utf-8");

const galaxyTerms = extractTerms(YAML.parse(galaxyYaml), [], "galaxy");
const toolUtilTerms = extractTerms(YAML.parse(toolUtilYaml), [], "tool-util");

// --- Group by category ---
const allTerms = [...toolUtilTerms, ...galaxyTerms];
const byCategory = new Map();

for (const entry of allTerms) {
  const cat = categoryLabel(entry.path);
  if (!byCategory.has(cat)) byCategory.set(cat, []);
  byCategory.get(cat).push(entry);
}

// Sort categories, putting tool-util categories first
const toolUtilCats = [];
const galaxyCats = [];
for (const [cat, entries] of byCategory) {
  if (entries.some((e) => e.source === "tool-util")) {
    toolUtilCats.push(cat);
  } else {
    galaxyCats.push(cat);
  }
}
toolUtilCats.sort();
galaxyCats.sort();
const sortedCats = [...toolUtilCats, ...galaxyCats];

// --- Generate markdown ---
const lines = [
  "<!-- DO NOT EDIT — generated by scripts/build-glossary.mjs from docs/glossary/*.yml -->",
  "",
  "# Glossary",
  "",
  "Terms used throughout galaxy-tool-util documentation. Project-specific terms are listed",
  "first, followed by general Galaxy terminology synced from",
  "[Galaxy's terms.yml](https://github.com/galaxyproject/galaxy/blob/dev/lib/galaxy/schema/terms.yml).",
  "",
  "---",
  "",
];

for (const cat of sortedCats) {
  const entries = byCategory.get(cat);
  entries.sort((a, b) => a.term.localeCompare(b.term));

  lines.push(`## ${cat}`);
  lines.push("");

  for (const entry of entries) {
    lines.push(`### ${titleCase(entry.term)}`);
    lines.push("");
    lines.push(entry.description);
    lines.push("");
  }
}

lines.push(
  "---",
  "",
  "*Terms under **Tool Util** are specific to galaxy-tool-util. Others are from Galaxy's upstream glossary.*",
  "",
);

writeFileSync(OUTPUT, lines.join("\n"));
console.log(`Generated ${OUTPUT} with ${allTerms.length} terms.`);
