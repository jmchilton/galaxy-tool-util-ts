/**
 * Inline all *.md.j2 and *.html.j2 files from src/workflow/templates/reports/
 * into a single TypeScript module (_templates-bundled.ts) so the published
 * dist/ is self-contained — consumers don't need to ship .j2 files.
 *
 * Run as: node scripts/bundle-templates.mjs
 * Wired into: "build": "node scripts/bundle-templates.mjs && tsc"
 */
import { readdir, readFile, writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

function escapeBackticks(src) {
  return src.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

// 1. Nunjucks report templates → src/workflow/_templates-bundled.ts
{
  const srcDir = join(__dirname, "../src/workflow/templates/reports");
  const outFile = join(__dirname, "../src/workflow/_templates-bundled.ts");

  const entries = await readdir(srcDir);
  const templateFiles = entries
    .filter((f) => f.endsWith(".md.j2") || f.endsWith(".html.j2"))
    .sort();

  const parts = ["// AUTO-GENERATED by scripts/bundle-templates.mjs — do not edit.\n"];
  parts.push("export const BUNDLED_TEMPLATES: Record<string, string> = {\n");

  for (const name of templateFiles) {
    const src = await readFile(join(srcDir, name), "utf-8");
    parts.push(`  ${JSON.stringify(name)}: \`${escapeBackticks(src)}\`,\n`);
  }

  parts.push("};\n");

  await writeFile(outFile, parts.join(""), "utf-8");
  console.log(`Bundled ${templateFiles.length} report templates → ${outFile}`);
}

// 2. Cytoscape HTML template → src/commands/cytoscapejs/_template-bundled.ts
{
  const srcFile = join(__dirname, "../src/commands/cytoscapejs/cytoscape.html");
  const outFile = join(__dirname, "../src/commands/cytoscapejs/_template-bundled.ts");
  const src = await readFile(srcFile, "utf-8");
  const out = [
    "// AUTO-GENERATED by scripts/bundle-templates.mjs — do not edit.\n",
    `export const CYTOSCAPE_HTML_TEMPLATE = \`${escapeBackticks(src)}\`;\n`,
  ].join("");
  await writeFile(outFile, out, "utf-8");
  console.log(`Bundled cytoscape template → ${outFile}`);
}
