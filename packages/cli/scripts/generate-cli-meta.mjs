/**
 * Generate browser-safe CLI metadata modules from the commander programs.
 *
 * Reads `dist/programs/*.js` (which must already be tsc-compiled when this
 * runs in the build flow), walks each commander program, and writes
 * `src/meta/_generated.ts` containing typed const data.
 *
 * Run as: node scripts/generate-cli-meta.mjs
 * Wired into: "build": "node scripts/bundle-templates.mjs && tsc && node scripts/generate-cli-meta.mjs && tsc"
 *
 * The double-tsc is intentional — first pass compiles meta-build + programs,
 * generator runs, second pass compiles _generated.ts into dist/.
 */
import { writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");

const { extractProgram } = await import(join(repoRoot, "dist/meta-build.js"));
const { buildGxwfProgram } = await import(join(repoRoot, "dist/programs/gxwf.js"));
const { buildGalaxyToolCacheProgram } = await import(
  join(repoRoot, "dist/programs/galaxy-tool-cache.js")
);

const gxwf = extractProgram(buildGxwfProgram());
const galaxyToolCache = extractProgram(buildGalaxyToolCacheProgram());

const out = `// AUTO-GENERATED by scripts/generate-cli-meta.mjs — do not edit.
// Source: src/programs/*.ts (commander program definitions).

import type { CliProgramSpec } from "./types.js";

export const gxwfCliMeta: CliProgramSpec = ${JSON.stringify(gxwf, null, 2)};

export const galaxyToolCacheCliMeta: CliProgramSpec = ${JSON.stringify(galaxyToolCache, null, 2)};
`;

const outFile = join(repoRoot, "src/meta/_generated.ts");
await writeFile(outFile, out, "utf8");

console.log(
  `Wrote ${outFile} (gxwf: ${gxwf.commands.length} commands, galaxy-tool-cache: ${galaxyToolCache.commands.length} commands)`,
);
